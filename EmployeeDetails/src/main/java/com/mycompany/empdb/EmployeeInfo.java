    private void btn_imageuploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_imageuploadActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();
        filename = f.getAbsolutePath();
        txt_imagepath.setText(filename);
        
        try {
            File image = new File(filename);
            BufferedImage thumbnail = Thumbnails.of(image).size(180, 220).asBufferedImage(); //create image data
            ByteArrayOutputStream os = new ByteArrayOutputStream(); // creating a buffer
            ImageIO.write(thumbnail, "jpeg", os); // writing the image
            InputStream is = new ByteArrayInputStream(os.toByteArray()); //receiving the data from the output stream
            
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024]; // 1 MB
            
            // read() decodes the data from the input stream, returning int ranged 0-255, else returns -1
            for(int readNum;(readNum = is.read(buf))!=-1;){
                bos.write(buf, 0, readNum);
            }
            viewImage = new ImageIcon(thumbnail); // creating the image icon object
            imageLabel.setIcon(viewImage); // placing the image into the label 
            
            employeeimage = bos.toByteArray();
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_imageuploadActionPerformed

