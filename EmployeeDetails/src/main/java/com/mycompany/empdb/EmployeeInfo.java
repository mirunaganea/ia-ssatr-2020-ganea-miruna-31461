    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        int tableClick = Integer.parseInt(jTable1.getModel().getValueAt(row, 9).toString());
        
        try {
             rs = db.tableClicked(tableClick);
            
            if(rs.next()){
                String strId = rs.getString("ID");
                txt_id.setText(strId);
                
                String strName = rs.getString("NAME");
                txt_name.setText(strName);
                
                String strGender = rs.getString("GENDER");
                if("Male".equals(strGender)) {
                    jRadioButton_Male.setSelected(true);
                }
                if("Female".equals(strGender)) {
                    jRadioButton_Female.setSelected(true);
                }
                
                String strAge = rs.getString("AGE");
                txt_age.setText(strAge);

                String strBg = rs.getString("BLOODGROUP");
                txt_bg.setText(strBg);

                String strContact = rs.getString("CONTACTNO");
                txt_contact.setText(strContact);


                String strQ =rs.getString("QUALIFICATION");
                jComboBox_q.setSelectedItem(strQ);


                String dateval = rs.getString("DATEJOINED");
                java.util.Date date=new SimpleDateFormat("yyyy-MM-dd").parse(dateval);
                jDateChooser1.setDate(date);

                String strAddress = rs.getString("ADDRESS");
                jTextArea_addr.setText(strAddress);


                byte[] imagedata = rs.getBytes("IMAGE");
                format = new ImageIcon(imagedata);
                imageLabel.setIcon(format);
                employeeimage=imagedata;
            }
            
        }
        catch(Exception e){
            
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try {
                rs.close();
            } catch (Exception e) {
            }
        }
        btn_save.setEnabled(false);
        btn_new.setEnabled(true);
        btn_update.setEnabled(true);
        btn_delete.setEnabled(true);
        btn_clear.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked

